"""Нормализация телефонных номеров к каноническому виду.

Используется как единый ключ для дедупликации пациентов и связывания
заявок (Lead) с пациентами вне зависимости от формата ввода
(«8 (999) 123-45-67», «+7 999 123 45 67», «79991234567» → +79991234567).
"""
import re


def normalize_phone(raw) -> str:
    """Привести номер к каноническому виду.

    Возвращает строку вида '+7XXXXXXXXXX' для российских/казахстанских
    номеров либо '+<digits>' для прочих. Пустая строка — если цифр нет.
    """
    if not raw:
        return ""
    digits = re.sub(r"\D", "", str(raw))
    if not digits:
        return ""
    # 11 цифр с ведущей 8 или 7 → код страны +7
    if len(digits) == 11 and digits[0] in ("7", "8"):
        return "+7" + digits[1:]
    # 10 цифр — номер без кода страны
    if len(digits) == 10:
        return "+7" + digits
    # прочие международные номера — просто с плюсом
    return "+" + digits


def phone_core(raw) -> str:
    """Последние 10 значащих цифр номера — для грубого отбора кандидатов
    при сравнении номеров, сохранённых в разных форматах."""
    norm = normalize_phone(raw)
    return norm[-10:] if norm else ""
